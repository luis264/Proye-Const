<?php

//Api.php

class API
{
	private $connect = '';

	function __construct()
	{
		$this->database_connection();
	}

	function database_connection()
	{
		$this->connect = new PDO("mysql:host=localhost;dbname=constructora", "root", "");
	}

	function fetch_all()
	{
		$query = "SELECT * FROM tipo_trabajo ORDER BY id_tipo;";
		$statement = $this->connect->prepare($query);
		if($statement->execute())
		{
			while($row = $statement->fetch(PDO::FETCH_ASSOC))
			{
				$data[] = $row;
			}
			return $data;
		}
	}

	function insert()
	{
		if(isset($_POST["nom_tipo"]))
		{
			$form_data = array(
				':nom_tipo'		    =>	$_POST["nom_tipo"]
				
			);
			$query = "
			INSERT INTO tipo_trabajo
			(nom_tipo) VALUES 
			(:nom_tipo);
			";
			$statement = $this->connect->prepare($query);
			if($statement->execute($form_data))
			{
				$data[] = array(
					'success'	=>	'1'
				);
			}
			else
			{
				$data[] = array(
					'success'	=>	'0'
				);
			}
		}
		else
		{
			$data[] = array(
				'success'	=>	'0'
			);
		}
		return $data;
	}

	function fetch_single($id)
	{
		$query = "SELECT * FROM tipo_trabajo where id_tipo='".$id."'";
		$statement = $this->connect->prepare($query);
		if($statement->execute())
		{
			foreach($statement->fetchAll() as $row)
			{
				$data['id_tipo'] = $row['id_tipo'];
				$data['nom_tipo'] = $row['nom_tipo'];
				
			}
			return $data;
		}
	}

	function update()
	{
		if(isset($_POST["nom_tipo"]))
		{
			$form_data = array(
				':nom_tipo'		    =>	$_POST["nom_tipo"],
				':id'	            =>	$_POST["hidden_id"]
			);
			$query = "
			UPDATE tipo_trabajo 
			SET nom_tipo = :nom_tipo
				 
			WHERE id_tipo = :id
			";
			$statement = $this->connect->prepare($query);
			if($statement->execute($form_data))
			{
				$data[] = array(
					'success'	=>	'1'
				);
			}
			else
			{
				$data[] = array(
					'success'	=>	'0'
				);
			}
		}
		else
		{
			$data[] = array(
				'success'	=>	'0'
			);
		}
		return $data;
	}
	function delete($id)
	{
		$query = "DELETE FROM tipo_trabajo WHERE id_tipo = '".$id."'";
		$statement = $this->connect->prepare($query);
		if($statement->execute())
		{
			$data[] = array(
				'success'	=>	'1'
			);
		}
		else
		{
			$data[] = array(
				'success'	=>	'0'
			);
		}
		return $data;
	}
}

